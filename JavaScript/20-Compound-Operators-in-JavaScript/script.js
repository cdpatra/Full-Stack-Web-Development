// ! <<=====================<< <| Compound(Combined Assignment) Operators |> >>===========================>>

let num = 5;
num += 5; // num = num + 5
console.log(num); //output-> 10

num = 5;
num -= 5; //num = num - 5
console.log(num); //output-> 0

num = 5;
num %= 5; // num = num % 5
console.log(num); //output-> 0

num = 5;
num *= 5; //num = num * 5
console.log(num); //output-> 25

num = 5;
num /= 5; //num = num / 5
console.log(num); //output-> 1

num = 5;
num **= 5; //num = num ** 5
console.log(num); //output-> 3125

// ! <<=======================<< <| INCREMENT OPERATOR |> >>===========================>>
num = 5;
// ~ post increment
console.log(num++); //output-> 5
console.log(num); //output-> 6
// ~ pre increment
console.log(++num); //output-> 7
console.log(num); //output-> 7

// ! <<======================<< <| DECREMENT OPERATOR |> >>============================>>
num = 5;
// ~ post decrement
console.log(num--); //output-> 5
console.log(num); //output-> 4
// ~ pre decrement
console.log(--num); //output-> 3
console.log(num); //output-> 3
